// Escreva um programa que recebe a idade de uma pessoa e imprime se ela é maior ou menor de idade(18 anos)

var idade = prompt("Digite sua idade");

if (idade >= 18) {
    alert("Você eh maior de idade")
    console.log("Você eh maior de idade")
}
else {
    alert("Você eh menor de idade")
    console.log("Você eh menor de idade")
}