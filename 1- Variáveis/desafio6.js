//Use operadores lógicos(&&, ||, !) para combinar expressões e exibir o resultado.

let resultadoAnd = true && true;
let resultadoOr = false || true;
let verdadeiro = !false;

console.log("true && true:", resultadoAnd);
console.log("true || false:", resultadoOr);
console.log("!false:", verdadeiro)
